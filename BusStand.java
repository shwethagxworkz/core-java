public class BusStand {
    private String busStandName;
    private int numberOfBuses;
    private int numberOfPlatforms;
    private int numberOfPassengers;
    private String addressOfBusStand;
    private String colorOfBuses;
    private String typeOfBus;

    public void setBusStandName(String busStandName) {
        this.busStandName = busStandName;
    }

    public String getBusStandName() {
        return busStandName;
    }

    public void setNumberOfBuses(int numberOfBuses) {
        this.numberOfBuses = numberOfBuses;
    }

    public int getNumberOfBuses() {
        return numberOfBuses;
    }

    public void setNumberOfPlatforms(int numberOfPlatforms) {
        this.numberOfPlatforms = numberOfPlatforms;
    }

    public int getNumberOfPlatforms() {
        return numberOfPlatforms;
    }

    public void setNumberOfPassengers(int numberOfPassengers) {
        this.numberOfPassengers = numberOfPassengers;
    }

    public int getNumberOfPassengers() {
        return numberOfPassengers;
    }

    public void setAddressOfBusStand(String addressOfBusStand) {
        this.addressOfBusStand = addressOfBusStand;
    }

    public String getAddressOfBusStand() {
        return addressOfBusStand;
    }

    public void setColorOfBuses(String colorOfBuses) {
        this.colorOfBuses = colorOfBuses;
    }

    public String getColorOfBuses() {
        return colorOfBuses;
    }

    public void setTypeOfBus(String typeOfBus) {
        this.typeOfBus = typeOfBus;
    }

    public String getTypeOfBus() {
        return typeOfBus;
    }

  }