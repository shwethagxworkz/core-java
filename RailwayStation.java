public class RailwayStation {
    private String railwayStationName;
    private int numberOfPlatforms;
    private String location;
    private int numberOfTicketCounters;
    private int numberOfEntryTerminals;
    private int numberOfTrains;
    private int numberOfEmployees;

    public void setRailwayStationName(String railwayStationName) {
        this.railwayStationName = railwayStationName;
    }

    public String getRailwayStationName() {
        return railwayStationName;
    }

    public void setNumberOfPlatforms(int numberOfPlatforms) {
        this.numberOfPlatforms = numberOfPlatforms;
    }

    public int getNumberOfPlatforms() {
        return numberOfPlatforms;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getLocation() {
        return location;
    }

    public void setNumberOfTicketCounters(int numberOfTicketCounters) {
        this.numberOfTicketCounters = numberOfTicketCounters;
    }

    public int getNumberOfTicketCounters() {
        return numberOfTicketCounters;
    }

    public void setNumberOfEntryTerminals(int numberOfEntryTerminals) {
        this.numberOfEntryTerminals = numberOfEntryTerminals;
    }

    public int getNumberOfEntryTerminals() {
        return numberOfEntryTerminals;
    }

    public void setNumberOfTrains(int numberOfTrains) {
        this.numberOfTrains = numberOfTrains;
    }

    public int getNumberOfTrains() {
        return numberOfTrains;
    }

    public void setNumberOfEmployees(int numberOfEmployees) {
        this.numberOfEmployees = numberOfEmployees;
    }

    public int getNumberOfEmployees() {
        return numberOfEmployees;
    }

    }